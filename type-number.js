// Tipo Number

const primeiroNumero = 5;
const segundoNumero = 7;

const adicao = primeiroNumero + segundoNumero;

console.log(adicao);

// Ponto Flutuante

const numeroPontoFlutuante = 3.3;
const pontoFlutuanteSemZero = 0.5;

const divisao = primeiroNumero / numeroPontoFlutuante;
const multiplicacao = segundoNumero * pontoFlutuanteSemZero;

console.log(divisao);
console.log(multiplicacao);

// NaN => Not A Number

const zelda = "zelda";

console.log(divisao - zelda);
